export GIT_COMMIT=`git rev-list -1 HEAD` 
echo "GIT_COMMIT=$GIT_COMMIT" | color-cat -c green

# export GIT_TAG=`git tag | tee ,a | sort -t "." -k1,1n -k2,2n -k3,3n | sed 's/_$//' | tee ,b | tail -1`
export GIT_TAG=`git tag | sort -t "." -k1,1n -k2,2n -k3,3n | sed 's/_$//' | tail -1`

if [ -z "$GIT_TAG" ] ; then
	export GIT_TAG="$GIT_COMMIT"
fi
echo "GIT_TAG=$GIT_TAG" | color-cat -c green
		
export BUILD_DATE="$(date)"

export GIT_AUTOJOB_APP_SERVER_REVISION="$GIT_COMMIT"

export GIT_BRANCH_NAME=$( git rev-parse --abbrev-ref HEAD )

cat >gitcommit.go <<ZZcc
package version

// Do Not Edit This File -- It was authomatically generated by ./bin/generate-git-commit.sh

// Generation Date: $(date)
// Generation On: $(hostname)

// Copyright (C) Philip Schlump 2026.
// MIT Licensed.  See LICENSE file.

var GitCommit string = "$GIT_COMMIT"
var Version string = "$GIT_TAG"
var BuildDate string = "$BUILD_DATE"
var GitBranchName string = "$GIT_BRANCH_NAME"

/* vim: set noai ts=4 sw=4: */
ZZcc
